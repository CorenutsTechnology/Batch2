package com.cnts.nov8;

import java.util.Arrays;

public class AllDataTypesArray
{
	public static void main(String[] args) {
		
		int[] intArray = {1,2,3,4};
		byte[] buteArray = {1,2,3,4};
		short[] shortArray = {1,2,3,4};
		long[] longArray = {17126,227438,33473,44349};
		float[] floatArray = {1.2f,17.3f,23.76f,2.72f,372};
		double[] doubleArray = {11.11,22.22,33.33,44.44,55.55};
		boolean[] booleanArray = {true,false,true,true};
		char[] charArray = {'a','n','i','s','h','a'};
		
		System.out.println(Arrays.toString(intArray));
		System.out.println(Arrays.toString(buteArray));
		System.out.println(Arrays.toString(shortArray));
		System.out.println(Arrays.toString(longArray));
		System.out.println(Arrays.toString(floatArray));
		System.out.println(Arrays.toString(doubleArray));
		System.out.println(Arrays.toString(booleanArray));
		System.out.println(Arrays.toString(charArray));
		
	}

}
