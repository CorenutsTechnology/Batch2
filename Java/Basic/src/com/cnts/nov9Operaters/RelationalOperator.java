package com.cnts.nov9Operaters;

public class RelationalOperator {
	public static void main(String[] args) {
		int value1 = 5;
		int value2 = 5;

		// ----ALL RELATIONAL OPERATOR GIVES BOOLEAN VALUE--

		// equalto operator ==
		System.out.println(value1 == value2);

		// notequalto operator !=
		System.out.println(value1 != value2);

		// greaterthen operator >
		System.out.println(value1 > value2);

		// lessthen operator <
		System.out.println(value1 < value2);

		// greaterthenequalto operator >=
		System.out.println(value1 >= value2);

		// lessthenequalto operator <=
		System.out.println(value1 <= value2);
	}
}
