package com.cnts.nov9Operaters;

public class UnaryOperaters
{
	public static void main(String[] args) {
		int number = 10;
		
		//pre-increment operator
		++number;
		System.out.println("Pre-Increment of Number:"+number );
		
		//post-increment operator
		number++;
		System.out.println("Post-Increment of Number:"+number);
		
		//pre-decrement operator
		--number;
		System.out.println("Pre-Decrement of number:"+number);
		
		//post-decrement operator
		number--;
		System.out.println("Post-Decrement of number:"+number);
	}
	
}
