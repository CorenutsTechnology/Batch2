package com.exception_overriding.nov21;

public class Person2 
{
	private String name;
	private int age;
	private char gender;
	
	public Person2(String name, int age, char gender) {
		super();
		this.name = name;
		this.age = age;
		this.gender = gender;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public char getGender() {
		return gender;
	}

	public void setGende(char gender) {
		this.gender = gender;
	}

	

	
}
