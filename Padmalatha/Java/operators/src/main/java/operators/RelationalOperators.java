package operators;

public class RelationalOperators {
	
	public boolean lessThan(int value1,int value2)
	{
		return value1<value2;
	}
	
	public boolean greaterThan(int value1,int value2)
	{
		return value1>value2;
	}

	public boolean equal(int value1,int value2)
	{
		return value1==value2;
	}

	
}
