package org.december11;

public class Calculator {
	
	public void sum(int value1,int value2) {
		System.out.println("Sum : "+(value1+value2));
	}
	
	public void diff(int value1,int value2) {
		System.out.println("Difference : "+(value1-value2));
	}
	
	public int multiplication(int value1,int value2) {
		return value1*value2;
	}

}
