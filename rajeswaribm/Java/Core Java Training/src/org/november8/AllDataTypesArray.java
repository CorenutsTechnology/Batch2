package org.november8;

public class AllDataTypesArray {

	public static void main(String[] args) {
		
		byte[] byteArray = new byte[3];
		byteArray[0]=2;
		byteArray[1]=3;
		byteArray[2]=6;
		
		System.out.println(byteArray[0]);
		System.out.println(byteArray[1]);
		System.out.println(byteArray[2]);
		System.out.println();
		
		short[] shortArray = new short[3];
		shortArray[0]=26;
		shortArray[1]=52;
		shortArray[2]=76;
		
		System.out.println(shortArray[0]);
		System.out.println(shortArray[1]);
		System.out.println(shortArray[2]);
		System.out.println();
		
		int[] intArray = new int[3];
		intArray[0]=23725;
		intArray[1]=32863;
		intArray[2]=69528;
		
		System.out.println(intArray[0]);
		System.out.println(intArray[1]);
		System.out.println(intArray[2]);
		System.out.println();
		
		long[] longArray = new long[3];
		longArray[0]=26652863l;
		longArray[1]=5263864438l;
		longArray[2]=76529649439l;
		
		System.out.println(longArray[0]);
		System.out.println(longArray[1]);
		System.out.println(longArray[2]);
		System.out.println();
		
		float[] floatArray = new float[3];
		floatArray[0]=2638.63f;
		floatArray[1]=34384.538f;
		floatArray[2]=6339.53f;
		
		System.out.println(floatArray[0]);
		System.out.println(floatArray[1]);
		System.out.println(floatArray[2]);
		System.out.println();
		
		double[] doubleArray = new double[3];
		doubleArray[0]=26;
		doubleArray[1]=52;
		doubleArray[2]=76;
		
		System.out.println(doubleArray[0]);
		System.out.println(doubleArray[1]);
		System.out.println(doubleArray[2]);
		System.out.println();
		
		boolean[] booleanArray = {true,false};
		
		System.out.println(booleanArray[0]);
		System.out.println(booleanArray[1]);
		System.out.println();
		
		char[] charArray = {100,'e',76};
		
		System.out.println(charArray[0]);
		System.out.println(charArray[1]);
		System.out.println(charArray[2]);
		System.out.println();
		
		String [] stringArray= {"Rajeswari","Lakshmi","Krishna","Uma"};
		
		System.out.println(stringArray[0]);
		System.out.println(stringArray[1]);
		System.out.println(stringArray[2]);
		System.out.println(stringArray[3]);
	}

}
