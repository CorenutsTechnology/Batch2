package org.november8;

public class DeclaringArray {

	public static void main(String[] args) {
				
		float floatArray[] = new float[3];	//array declaration and instantiation
		floatArray[0]=2638.63f;
		floatArray[1]=34384.538f;
		floatArray[2]=6339.53f;

		long[] longArray = {738964l,86332189l,682293938l};	//array declaration without size
		
		char []charArray;			//array declaration
		charArray = new char[5];	//array instantiation
		charArray[0]='g';			//array initialization
		charArray[1]=106;
		
		int []intArray = new int[] {272,62862,862};
		
	}

}
