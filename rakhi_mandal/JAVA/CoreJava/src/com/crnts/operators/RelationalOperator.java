package com.crnts.operators;

public class RelationalOperator {
	 public boolean smaller(int value1,int value2)
	   {
		   boolean result=(value1<value2);
		  
		   return result ;
	   }
	 public boolean greater(int value1,int value2)
	   {
		   boolean result=(value1>value2);
		   return result ;
	   }
	 public boolean smallerequal(int value1,int value2)
	   {
		   boolean result=(value1<=value2);
		   return result ;
	   }
	 public boolean greaterequal(int value1,int value2)
	   {
		   boolean result=(value1>=value2);
		   return result ;
	   }
	 public boolean doubleequal(int value1,int value2)
	   {
		   boolean result=(value1==value2);
		   return result ;
	   }
	 

}
