package com.cnuts.operator;

public class ArithmeticOperators {
	
	public int addition(int value1,int value2) {
		int result=value1+value2;
		System.out.println("addition result: "+result);
		return result;
	}
	
	public int subtraction(int value1,int value2) {
		int result=value1-value2;
		System.out.println("subtraction result: "+result);
		return result;
	}
	
	public int multipication(int value1,int value2) {
		int result=value1*value2;
		System.out.println("multipication result: "+result);
		return result;
	}
	
	public double division(double value1,double value2) {
		double result=value1/value2;
		System.out.println("division result: "+result);
		return result;
	}
	
	public int moduler(int value1,int value2) {
		int result=value1%value2;
		System.out.println("moduler result: "+result);
		return result;
	}
	
	
	
	

}
