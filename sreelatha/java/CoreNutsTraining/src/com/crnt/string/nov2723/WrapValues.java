package com.crnt.string.nov2723;

public class WrapValues {
	public void wrapTest()
	{
		Integer integer = new Integer("34");
		System.out.println(integer);
		
		Double doubleValue = new Double("23.5");
		System.out.println(doubleValue);
		
		Long longValue = new Long("234354647");
		System.out.println(longValue);
		
		Character charValue = new Character('r');
		System.out.println(charValue);
		
		
		Short shortValue = new Short("34");
		System.out.println(shortValue);
	}

}
