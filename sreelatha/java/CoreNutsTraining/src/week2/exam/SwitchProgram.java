package week2.exam;

public class SwitchProgram {
	public static void main(String[] args) {
		
	        int value = 2;
	        switch(value) {
	        case 1:
	        	 System.out.println("ONE");
	        	// continue;
			case 2:
	            
	            System.out.println("TWO");
	           // continue;
	        case 3:
	            
	            System.out.println("Three");
	            break;
	        
	}

}
}
