package day_09_11_23_Assignment;

public class RelationalOperators {
	
	public boolean less(int value1,int value2) {
		return value1<value2;
	}
	
	public boolean lessThanOrEqual(int value1,int value2) {
		return value1<=value2;
	}
	
	public boolean greater(int value1,int value2) {
		return value1>value2;
	}
	
	public boolean greaterThanOrEqual(int value1,int value2) {
		return value1>=value2;
	}
	
	public boolean equal(int value1,int value2) {
		return value1==value2;
	}
	
	public boolean notEqual(int value1,int value2) {
		return value1!=value2;
	}
}
