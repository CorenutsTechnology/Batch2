package day_27_11_23_Assignment;


public class Q7_PrimitiveToWrapperClass {

	public static void main(String[] args) {
		Integer value1=new Integer(121);
		int value2=value1;
		System.out.println(value2);
		
		Integer value3=15;
		System.out.println(value3);
		
		Long value4=new Long("56");
		long value5=value4;
		System.out.println(value5);
		
		long value6=23;
		Long value7=value6;
		System.out.println(value7);
		
	}

}
